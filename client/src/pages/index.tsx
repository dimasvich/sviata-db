import Button from '@/components/ui/Button';
import Layout from '@/components/ui/Layout';
import Pagination from '@/components/ui/Pagination';
import SviatoCard from '@/components/ui/sviato/SviatoCard';
import Typography from '@/components/ui/Typography';
import { getList } from '@/http/crud';
import Head from 'next/head';
import { useRouter } from 'next/navigation';
import { useEffect, useState } from 'react';

export default function Home() {
  const router = useRouter();
  const [list, setList] = useState([]);
  const [currentPage, setCurrentPage] = useState(1);
  const [totalPages, setTotalPages] = useState(1);

  useEffect(() => {
    const fetch = async () => {
      const res = await getList(currentPage);
      if (!res.ok) {
        alert('Виникла помилка під час отримання даних з БД');
        return;
      }
      const json = await res.json();
      setList(json.data);
      setTotalPages(json.totalPages);
    };
    fetch();
  }, [currentPage]);

  return (
    <>
      <Head>
        <title>Sviato-db | Dashboard</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>
        <div className="flex w-full justify-between">
          <Typography type="title">Всі свята</Typography>
          <Button
            onClick={() => {
              router.push('/create-sviato');
            }}
          >
            + Нове свято
          </Button>
        </div>
        <div className="my-4">
          {list.map(
            (item: { name: string; timestamp: string; _id: string }) => (
              <SviatoCard
                key={item._id}
                id={item._id}
                name={item.name}
                date={item.timestamp}
              />
            ),
          )}
        </div>
        {totalPages > 1 && (
          <Pagination
            totalPages={totalPages}
            currentPage={currentPage}
            onChange={(p) => setCurrentPage(p)}
          />
        )}
      </Layout>
    </>
  );
}
